.STACK 64

.DATA
VAR DW ?
VAR2 DW ?
VAR3 DB ?
VAR4 DW ?

.CODE
PRIN PROC FAR
	CALL SUMA	; CALL D)

	MOV AX,4C00H	; Salir a MS-DOS
	INT 21H
PRIN ENDP

; A) 400 - 20 = VAR (REG,INM)
SUBS PROC NEAR
	CALL HIB	; CALL E)
	
	MOV AX,400
	SUB AX,20
	MOV VAR,AX
	
	RET
SUBS ENDP

; B) 55 * 3 = DX (MEM)
MULT PROC NEAR
	CALL SUBS	; CALL A)

	MOV AL,3
	MOV VAR2,55
	MUL VAR2
	MOV DX,AX

	RET
MULT ENDP

; C) 60 / 6 = VAR (MEM)
DIVI PROC NEAR
	MOV AX,60
	MOV VAR3,6
	DIV VAR3
	MOV VAR3,AL

	RET 
DIVI ENDP

; D) 17 + 777 = VAR (REG,REG)
SUMA PROC NEAR
	CALL MULT	; CALL B)

	MOV BX,17
	MOV CX,777
	ADD BX,CX
	MOV VAR4,BX

	RET
SUMA ENDP

; E) ([80 + 90 + 7] - 20) / 2 = DH (REG)
HIB PROC NEAR
	CALL DIVI	; CALL C)

	MOV AX,80
	ADD AX,90
	ADD AX,7
	SUB AX,20
	MOV BL,2
	DIV BL
	MOV DH,AL
	
	RET
HIB ENDP

END PRIN